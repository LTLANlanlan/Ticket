package ui;
import java.awt.*;
import javax.swing.*;
import java.awt.event.*;
public class InButton {
InButton(JButton but_in,JFrame frame,String frame2){
	but_in.addActionListener(new ActionListener() {
		public void actionPerformed(ActionEvent e) {
			if(e.getSource()==but_in) {
				frame.dispose();
				if(frame2=="查询")new FindFrame();
				else
				if(frame2== "抢票") {}
				else
				if(frame2=="订单") new MyOrderFrame();
				else
				if(frame2=="个人信息")new UserFrame();
					
				}
		}
	});
}
}
